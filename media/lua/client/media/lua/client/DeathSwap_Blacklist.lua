DeathSwap = DeathSwap or {}


function DeathSwap.getBlacklist()
    return SandboxVars.DeathSwap.Blacklist or "Brollow;Admin"
end

function DeathSwap.isBlacklisted(user) --/dsCheck username
    if instanceof(user, "IsoPlayer") then
        user = user:getUsername()
    end
    for _, name in ipairs(DeathSwap.parseList()) do
        if name == user then
            return true
        end
    end
    return false
end

function DeathSwap.addUserBlacklist(user) --/dsAdd username
    if instanceof(user, "IsoPlayer") then
        user = user:getUsername()
    end
    if not DeathSwap.isBlacklisted(user) then
        local blacklist = DeathSwap.getBlacklist() .. ";" .. user
        DeathSwap.saveBlacklist(blacklist)
    end
end


function DeathSwap.delUserBlacklist(user) --/dsDel username
    if instanceof(user, "IsoPlayer") then
        user = user:getUsername()
    end
    local names = DeathSwap.parseList()
    local newList = {}

    for _, name in ipairs(names) do
        if name ~= user then
            table.insert(newList, name)
        end
    end

    DeathSwap.saveBlacklist(table.concat(newList, ";"))
end


function DeathSwap.saveBlacklist(blacklist)
    if tostring(DeathSwap.getBlacklist()) ~= tostring(blacklist) then
        local sOpt = getSandboxOptions()
        sOpt:getOptionByName("DeathSwap.Blacklist"):setValue(tostring(blacklist))
        sOpt:toLua()
        sOpt:sendToServer()
    end
end

function DeathSwap.parseList() --preserves spaces
    local blacklist = DeathSwap.getBlacklist()
    local names = {}
    for name in string.gmatch(blacklist, "([^;]+)") do
        table.insert(names, name)
    end
    return names
end

